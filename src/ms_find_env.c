#include "../inc/minishell.h"

int		ms_find_env(char *av, char ***env)
{
	int		i;

	i = 0;
	while (env[0][i])
	{
		if (ft_strzstr(env[0][i], av))
			return (i);
		i++;
	}
	return (-1);
}