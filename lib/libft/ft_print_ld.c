#include "libft.h"

int		ft_print_ld(long int num)
{
	int		neg;

	neg = 0;
	if (num < -9223372036854775807)
	{
		ft_putstr("-9223372036854775808");
		return (20);
	}
	if (num < 0)
		neg = 1;
	ft_putnbr(num);
	return (ft_intlen(num) + neg);
}
